package easy.work.source.module.rights.pojo;

public class RolePremission {
	private Integer rolePremissionId;

	private Integer rid;

	private Integer pid;

	private Integer flag;
	
	public Integer getFlag() {
		return flag;
	}

	public void setFlag(Integer flag) {
		this.flag = flag;
	}

	public Integer getRolePremissionId() {
		return rolePremissionId;
	}

	public void setRolePremissionId(Integer rolePremissionId) {
		this.rolePremissionId = rolePremissionId;
	}

	public Integer getRid() {
		return rid;
	}

	public void setRid(Integer rid) {
		this.rid = rid;
	}

	public Integer getPid() {
		return pid;
	}

	public void setPid(Integer pid) {
		this.pid = pid;
	}
}